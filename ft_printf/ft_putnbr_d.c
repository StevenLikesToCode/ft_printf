#include "ft_printf.h"

void	ft_putnbr_d(int n, int *l)
{
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*l) += 11;
		return ;
	}
	if (n < 0)
	{
		ft_putchar_len('-', l);
		ft_putnbr_d(n * -1, l);
	}
	else
	{
		if (n >= 10)
		{
			ft_putnbr_d(n / 10, l);
		}
		ft_putchar_len(n % 10 + '0', l);
	}
}
